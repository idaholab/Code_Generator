/**
 * \mainpage The ns-3 topology generator documentation
 *
 * \section section-intro Introduction
 *
 * This is the API documentation of ns-3-generator, an open-source GUI topology builder for ns-3.
 *
 * It provides intuitive GUI so you can add nodes, link, applications, ... and generates C++ simulation
 * code for ns-3. Python generated code could be available in the future.
 * 
 * \section section-modules Modules
 *
 * The API is decomposed in several modules:
 * - GUI (located in gui/);
 * - Core (located in kern/).

 * There are basics unit tests in test directory. Note that you have to install <a href="http://cppunit.sourceforge.net/">CppUnit</a>
 * framework in order to use it.
 *
 * \section section-standard Standards
 *
 * ns-3-generator is written in pure C++ language for the core part. GUI part use Qt 4 framework (C++).
 *
 * ns-3-generator should work on any system which (could) have Qt 4 libraries (i.e. Linux, Microsoft Windows, Mac OS X).
 */

